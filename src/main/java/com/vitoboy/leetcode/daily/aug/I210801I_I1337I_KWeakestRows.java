    public int[] kWeakestRows(int[][] mat, int k) {
        int m = mat.length, n = mat[0].length;
        int[][] tmp = new int[m][2];
        for (int i = 0; i < m; i++) {
            int sum = 0;
            for (int j = 0; j < n; j++) {
                if (mat[i][j] > 0) {
                    sum += mat[i][j];
                } else {
                    break;
                }
            }
            tmp[i] = new int[]{i, sum};
        }
        Arrays.sort(tmp, ((o1, o2) -> {
            if(o1[1] == o2[1]) {
                return Integer.compare(o1[0], o2[0]);
            } else {
                return Integer.compare(o1[1],o2[1]);
            }
        }));
        int[] ans = new int[k];
        for (int i = 0; i <k; i++) {
            ans[i] = tmp[i][0];
        }
        return ans;
    }
