    public int findPoisonedDuration(int[] timeSeries, int duration) {
        int sum = 0, tmp = 0;
        for (int i = 0, len = timeSeries.length; i < len; i++) {
            if (tmp > timeSeries[i]) {
                sum = sum - (tmp - timeSeries[i]) + duration;
            } else {
                sum += duration;
            }
            tmp = timeSeries[i]+duration;
        }
        return sum;
    }
